  <h1>Whitepaper Mystic Nusa</h1>

  <section>
    <h2>1. Pendahuluan</h2>
    <p>Mystic Nusa adalah proyek komunitas berbasis budaya dan mistisisme Indonesia yang dikemas dalam bentuk token kripto dan ekosistem Web3. Terinspirasi dari legenda urban, mitos lokal, dan nilai-nilai mistik nusantara, Mystic Nusa bertujuan untuk menghidupkan kembali kisah-kisah tersebut dalam platform komunitas digital dan hiburan.</p>
  </section>

  <section>
    <h2>2. Masalah yang Ingin Diselesaikan</h2>
    <ul>
      <li>Kurangnya token lokal berbasis budaya yang kuat dan relevan.</li>
      <li>Komunitas Web3 Indonesia yang terfragmentasi dan belum termonetisasi dengan baik.</li>
      <li>Minimnya eksposur global terhadap kekayaan cerita mistik nusantara.</li>
    </ul>
  </section>

  <section>
    <h2>3. Solusi: Mystic Nusa</h2>
    <p>Mystic Nusa hadir sebagai ekosistem komunitas dengan token $MYNU sebagai penggerak utama. Token ini akan digunakan untuk reward harian, partisipasi komunitas, staking, voting, hingga mengakses konten eksklusif berbasis budaya lokal.</p>
  </section>

  <section>
    <h2>4. Visi & Misi</h2>
    <p><strong>Visi:</strong> Membangun platform komunitas Web3 berbasis budaya dan mistisisme Indonesia.</p>
    <p><strong>Misi:</strong></p>
    <ul>
      <li>Menghidupkan kembali kisah-kisah mistik nusantara melalui Web3 dan teknologi.</li>
      <li>Memberdayakan komunitas kreator, kolektor, dan penggemar cerita horor lokal.</li>
      <li>Menyediakan platform insentif komunitas berbasis token dan NFT.</li>
    </ul>
  </section>

  <section>
    <h2>5. Tokenomics</h2>
    <p>Distribusi token $MYNU dilakukan secara transparan dengan alokasi untuk airdrop, staking, reward, likuiditas, ekosistem, treasury, dan tim. Vesting token diatur untuk menjaga kestabilan dan pertumbuhan jangka panjang.</p>
  </section>

  <section>
    <h2>6. Roadmap</h2>
    <ul>
      <li><strong>Q3 2025:</strong> Launch token, staking program, mini games, airdrop harian.</li>
      <li><strong>Q4 2025:</strong> Peluncuran staking reward, mini games, kolaborasi partner, listing DEX.</li>
      <li><strong>Q1 2026:</strong> Web3 wallet integration, leaderboard rewards, ekspansi kolaborasi lokal.</li>
      <li><strong>Q2–Q4 2026:</strong> Burn unclaimed token, DAO voting dan listing di bursa terdesentralisasi.</li>
    </ul>
  </section>

  <section>
    <h2>7. Utilitas Token</h2>
    <ul>
      <li><strong>Trading:</strong> Gunakan token untuk jual beli dalam ekosistem Mystic Nusa dan memperluas kepemilikan mistikmu.</li>
      <li><strong>Staking:</strong> Kunci tokenmu dan bangkitkan kekuatan ghaib. Raih imbalan dari dunia tak kasat mata.</li>
      <li><strong>GameFi:</strong> Mainkan game mistis, bertarung, dan kumpulkan token sambil menjelajahi legenda nusantara.</li>
      <li><strong>Akses Eksklusif:</strong> Buka gerbang rahasia, fitur premium, dan event yang hanya bisa diakses oleh pemilik token.</li>
      <li><strong>DAO Voting:</strong> Tentukan arah cerita dan fitur berikutnya. Tokenmu adalah suaramu.</li>
      <li><strong>Merch Swap:</strong> Tukar tokenmu untuk mendapatkan merchandise eksklusif bertema mistis.</li>
    </ul>
  </section>

  <section>
    <h2>8. Partisipasi Komunitas</h2>
